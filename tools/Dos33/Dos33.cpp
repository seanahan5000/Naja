
#include <windows.h>
#include <stdio.h>
#include "Dos33.h"

const INT32 kBytesPerDisk = 35 * 16 * 256;
extern UINT8 sDOS33[(16 + 16 + 5) * 256];
extern UINT8 sVTOC[256];

//------------------------------------------------------------------------------

Dos33::Dos33()
{
	mFile = NULL;
	mImage = NULL;
}


Dos33::~Dos33()
{
	if (mFile)
		Close();
	if (mImage)
		delete [] mImage;
}


bool
Dos33::Open(const char* fileName,bool create)
{
	mFile = fopen(fileName,create ? "wb+" : "rb+");
	if (!mFile)
		return false;
	
	mImage = new UINT8[kBytesPerDisk];
	if (!create)
	{
		size_t fileSize = fread(mImage,1,kBytesPerDisk,mFile);
		if (fileSize != kBytesPerDisk)
		{
			printf("Invalid file size (%d != %d)\n",fileSize,kBytesPerDisk);
			return false;
		}
	}
	return true;
}


bool
Dos33::Close()
{
	if (mFile)
	{
		fseek(mFile,0,SEEK_SET);
		if (fwrite(mImage,1,kBytesPerDisk,mFile) != kBytesPerDisk)
		{
			printf("File write failed\n");
			return false;
		}
		
		fclose(mFile);
		mFile = NULL;
	}
	return true;
}

//------------------------------------------------------------------------------

void
Dos33::Format()
{
	// clear everything
	memset(GetTrackSector(0,0),0,kBytesPerDisk);
	
	// copy image of DOS 3.3
	memcpy(GetTrackSector(0,0),sDOS33,sizeof(sDOS33));
	
	// copy empty VTOC
	memcpy(GetVTOC(),sVTOC,sizeof(sVTOC));
	
	// init 15 catalog sectors
	for (INT32 s = 15; s > 1; --s)
	{
		UINT8* ptr = GetTrackSector(17,s);
		ptr[1] = 17;
		ptr[2] = s - 1;
	}
}

//------------------------------------------------------------------------------

static bool
CatalogCallback(UINT8* fileEntry,void* context)
{
	UINT8 type = fileEntry[2];
	char locked = (type & 0x80) ? '*' : ' ';
	char fileType;
	
	switch (type & 0x7F)
	{
		case 0x00:
			fileType = 'T';
			break;
		case 0x01:
			fileType = 'I';
			break;
		case 0x02:
			fileType = 'A';
			break;
		case 0x04:
			fileType = 'B';
			break;
		case 0x08:
			fileType = 'S';
			break;
		case 0x10:
			fileType = 'R';
			break;
		case 0x20:
			fileType = 'X';		// 'A' type (not Applesoft)
			break;
		case 0x40:
			fileType = 'Y';		// 'B' type (not binary)
			break;
		default:
			fileType = '?';
			break;
	}
	
	INT32 length = fileEntry[0x21] + (fileEntry[0x22] << 8);
	
	char name[32];
	for (INT32 i = 0; i < 30; ++i)
		name[i] = 0x7F & fileEntry[0x03 + i];
	name[30] = 0;
	
	printf("%c%c %03d %s\n",locked,fileType,length,name);
	return true;
}


void
Dos33::Catalog()
{
	ForEachAllocatedFileEntry(CatalogCallback,NULL);
}

//------------------------------------------------------------------------------

void
Dos33::DeleteFileEntry(UINT8* fileEntry)
{
	INT32 tsTrack = fileEntry[0];
	INT32 tsSector = fileEntry[1];
	UINT8* tslist = GetTrackSector(tsTrack,tsSector);
	while (true)
	{
		INT32 tsoffset = 0x0C;
		for (INT32 i = 0; i < 122; ++i)
		{
			INT32 t = tslist[tsoffset + 0];
			INT32 s = tslist[tsoffset + 1];
			if (t != 0 || s != 0)
				FreeTrackSector(t,s);
			tsoffset += 2;
		}
		
		INT32 tsNextTrack = tslist[1];
		INT32 tsNextSector = tslist[2];
		
		FreeTrackSector(tsTrack,tsSector);
		
		tsTrack = tsNextTrack;
		tsSector = tsNextSector;
		
		if (tsTrack == 0 && tsSector == 0)
			break;
		
		tslist = GetTrackSector(tsTrack,tsSector);
	}
	
	fileEntry[0x20] = fileEntry[0x00];
	fileEntry[0x00] = 0xFF;
}


bool
Dos33::Delete(const char* fileName)
{
	UINT8* fileEntry = FindFileEntry(fileName);
	if (!fileEntry)
	{
		printf("File not found\n");
		return false;
	}
	
	if (fileEntry[0x02] & 0x80)
	{
		printf("File locked\n");
		return false;
	}
	
	DeleteFileEntry(fileEntry);
	return true;
}

