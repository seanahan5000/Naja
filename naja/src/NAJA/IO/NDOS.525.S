				DB	$37,$38,$F8
				DB	$39,$3A,$3B
				DB	$3C,$3D,$3E
				DB	$3F
				
INTERLEAVE		DB	$00,$08,$01,$09,$02,$0A,$03,$0B
				DB	$04,$0C,$05,$0D,$06,$0E,$07,$0F
			else
				DB	$3F,$3E,$98
				DB	$99,$3D,$3C
				DB	$9C,$3B,$3A
				DB	$39,$A0,$A1
				DB	$A2,$A3,$A4
				DB	$A5,$14,$13
				DB	$A8,$A9,$12
				DB	$11,$10,$0F
				DB	$0E,$0D,$B0
