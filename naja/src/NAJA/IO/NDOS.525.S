				DB	$22,$1F,$1E
				DB	$1D,$1C,$1C
				DB	$1C,$1C,$1C

DELAYS			DB	$01,$30,$28
				DB	$24,$20,$1E
				DB	$1D,$1C,$1C
				DB	$1C,$1C,$1C

