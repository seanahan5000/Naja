static bool
BsaveCallback(UINT8* fileEntry,void* context)
{
	return false;		// always stop on first free
}


bool
Dos33::Bsave(const char* fileName,INT32 address,INT32 length,UINT8* srcData)
{
	UINT8* fileEntry;
	UINT8 name30[30];
	
	PrepareFileName(name30,fileName);
	
	fileEntry = FindFileEntry(name30);
	if (fileEntry)
		DeleteFileEntry(fileEntry);
	else
	{
		fileEntry = ForEachFreeFileEntry(BsaveCallback,NULL);
		if (!fileEntry)
		{
			printf("Disk full\n");
			return false;
		}
	}
	
	// Figure out how many sectors will be needed,
	//	including space for tsList sectors.
	INT32 sectorsNeeded = (length + 4 + 255) / 256;
	INT32 tsNeeded = (sectorsNeeded + 121) / 122;
	if (sectorsNeeded + tsNeeded > CountFreeSectors())
	{
		printf("Disk full\n");
		return false;
	}
	
	UINT8 tsTrack;
	UINT8 tsSector;
	UINT8* tsPtr = AllocateSector(&tsTrack,&tsSector);
	INT32 tsOffset = 0x0C;				// offset of first T/S entry
	INT32 sectorOffset = 0;
	
	// fill in fileEntry
	fileEntry[0x00] = tsTrack;
	fileEntry[0x01] = tsSector;
	fileEntry[0x02] = 0x04;				// unlocked binary file
	memcpy(&fileEntry[0x03],name30,30);
	fileEntry[0x21] = sectorsNeeded & 255;
	fileEntry[0x22] = sectorsNeeded >> 8;
	
	bool firstPass = true;
	while (length > 0)
	{
		INT32 byteCount;
		UINT8 track;
		UINT8 sector;
		UINT8* dp;
		
		dp = AllocateSector(&track,&sector);
		byteCount = 256;
		
		// if T/S sector is full, allocate another
		if (tsOffset == 256)
		{
			UINT8* nextTSPtr = AllocateSector(&tsTrack,&tsSector);
			sectorOffset += 122;
			tsPtr[0x01] = tsTrack;
			tsPtr[0x02] = tsSector;
			tsPtr[0x05] = sectorOffset & 255;
			tsPtr[0x06] = sectorOffset >> 8;
			tsPtr = nextTSPtr;
			tsOffset = 0x0C;			// offset of first T/S entry
		}
		
		// put track/sector into tsPtr
		tsPtr[tsOffset++] = track;
		tsPtr[tsOffset++] = sector;
		
		// write address for binary files
		if (firstPass)
		{
			*dp++ = address & 255;
			*dp++ = address >> 8;
			*dp++ = length & 255;
			*dp++ = length >> 8;
			byteCount -= 4;
			firstPass = false;
		}
		
		if (byteCount > length)
			byteCount = length;
		
		memcpy(dp,srcData,byteCount);
		srcData += byteCount;
		length -= byteCount;
	}
	
	return true;
}

//------------------------------------------------------------------------------

static bool
FindEntryCallback(UINT8* fileEntry,void* context)
{
	UINT8* name30 = (UINT8*)context;
	INT32 i;
	for (i = 0; i < 30; ++i)
	{
		if (fileEntry[0x03 + i] != name30[i])
			return true;	// continue searching
	}
	
	return false;			// stop searching
}


UINT8*
Dos33::FindFileEntry(UINT8* name30)
{
	return ForEachAllocatedFileEntry(FindEntryCallback,name30);
}


UINT8*
Dos33::FindFileEntry(const char* fileName)
{
	UINT8 name30[30];
	
	PrepareFileName(name30,fileName);
	return FindFileEntry(name30);
}


void
Dos33::PrepareFileName(UINT8* name30,const char* fileName)
{
	char* base = strrchr(fileName,'\\');
	if (base)
		fileName = base + 1;
	
	INT32 i = 0;
	const char* sp = fileName;
	char c;
	while ((c = *sp++) != 0)
	{
		name30[i++] = toupper(c) | 0x80;
		if (i == 30)
			break;
	}
	while (i < 30)
		name30[i++] = ' ' | 0x80;
}

//------------------------------------------------------------------------------

UINT8*
Dos33::ForEachAllocatedFileEntry(FileEntryProc proc,void* context)
{
	UINT8* vtoc = GetTrackSector(17,0);
	UINT8* cat = GetTrackSector(vtoc[1],vtoc[2]);
	
	while (true)
	{
		INT32 offset = 0x0B;
		do {
			if (cat[offset + 0] != 0x00 && cat[offset + 0] != 0xFF)
			{
				if (!proc(&cat[offset],context))
					return &cat[offset];
			}
			offset += 0x23;
		} while (offset < 0xFF);
		
		if (cat[1] == 0 && cat[2] == 0)
			break;
		
		cat = GetTrackSector(cat[1],cat[2]);
	}
	return NULL;
}


UINT8*
Dos33::ForEachFreeFileEntry(FileEntryProc proc,void* context)
{
	UINT8* vtoc = GetTrackSector(17,0);
	UINT8* cat = GetTrackSector(vtoc[1],vtoc[2]);
	
	while (true)
	{
		INT32 offset = 0x0B;
		do {
			if (cat[offset + 0] == 0x00 || cat[offset + 0] == 0xFF)
			{
				if (!proc(&cat[offset],context))
					return &cat[offset];
			}
			offset += 0x23;
		} while (offset < 0xFF);
		
		if (cat[1] == 0 && cat[2] == 0)
			break;
		
		cat = GetTrackSector(cat[1],cat[2]);
	}
	return NULL;
}

//------------------------------------------------------------------------------

INT32
Dos33::CountFreeSectors()
{
	INT32 count = 0;
	for (INT32 t = 0; t < 35; ++t)
	{
		for (INT32 s = 0; s < 16; ++s)
		{
			UINT8 mask;
			UINT8* ptr = GetAllocationByteAndMask(t,s,&mask);
			if (*ptr & mask)
				++count;
		}
	}
	return count;
}


UINT8*
Dos33::GetAllocationByteAndMask(INT32 track,INT32 sector,UINT8* mask)
{
	UINT8* vtoc = GetVTOC();
	
	INT32 offset = 0x38 + track * 4;
	if (sector < 8)
		offset += 1;
	
	*mask = 1 << (sector & 7);
	return &vtoc[offset];
}


UINT8*
Dos33::AllocateSector(UINT8* ptrack,UINT8* psector)
{
	UINT8* vtoc = GetVTOC();
	UINT8 track = vtoc[0x30];
	UINT8 firstTrack = track;
	bool forwardDone = false;
	bool backwardDone = false;
	while (!forwardDone && !backwardDone)
	{
		for (INT32 sector = 16; --sector >= 0; )
		{
			if (AllocateTrackSector(track,sector))
			{
				*ptrack = track;
				*psector = sector;
				vtoc[0x30] = track;
				return GetTrackSector(track,sector);
			}
		}
		track += vtoc[0x31];
		if (track == 35)
		{
			forwardDone = true;
			vtoc[0x31] = -vtoc[0x31];
			track = firstTrack;
		}
		else if (track == 0xFF)
		{
			backwardDone = true;
			vtoc[0x31] = -vtoc[0x31];
			track = firstTrack;
		}
	}
	return NULL;
}


bool
Dos33::AllocateTrackSector(INT32 track,INT32 sector)
{
	UINT8 mask;
	UINT8* ptr = GetAllocationByteAndMask(track,sector,&mask);
	
	if (!(*ptr & mask))
		return false;
	
	*ptr &= ~mask;
	return true;
}


bool
Dos33::FreeTrackSector(INT32 track,INT32 sector)
{
	UINT8 mask;
	UINT8* ptr = GetAllocationByteAndMask(track,sector,&mask);
	
	if (*ptr & mask)
		return false;
	
	*ptr |= mask;
	return true;
}

